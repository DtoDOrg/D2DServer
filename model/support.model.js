import mongoose, { Schema } from "mongoose";
const SupportSchema = new Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const SupportModel = mongoose.model("Support", SupportSchema);
export default SupportModel;
